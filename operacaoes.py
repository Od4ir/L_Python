# OPERAÇÕES:

# Resto, Divisão e Divisão Inteira:
print("Resto 40 % 2: ",40 % 2)
print("Divisão 40 / 2: ", 40 / 2)
print("Divisão Inteira 40//2: ", 40 // 2)

print("\nResto 40 % 6: ",40 % 6)
print("Divisão 40 / 6: ", 40 / 6)
print("Divisão Inteira 40//6: ", 40 // 6)

#Potenciação:
print("\nPotenciação 4**2: ", 4**2)
print("Potenciação 2.5**2: ", 2.5**2)
#Quando coloca a vírgula, o programa calcula a potência de 2 e 5, não de 2,5;
print("Potenciação 2,5**2: ", 2,5**2)
print("Potenciação 3,5,8**2: ", 3,5,8**2)

#1e2 é o mesmo que 10**2, 10e2 é o mesmo que 100**2;
print("Potência de 10 - 1e2: ", 1e2)
print("Potência de 100 - 10e1: ", 10e1)
